let idade = 35; 
console.log(typeof idade); // o typeof mostra o tipo da variável.

let salario = 20000.90; // o JavaScript não faz distinção entre números inteiros e decimais.
console.log(typeof salario); // salario aqui é uma variável do tipo number


let estaCalor = true; // variáveis boolean só aceitam dois valores: true ou false
console.log(typeof estaCalor); // aqui a variável estaCalor é do tipo boolean

let nome = "Alysson Melo"; // variáveis do tipo string aceitam qualquer tipo de texto, inclusive números dentro de aspas.
console.log(typeof nome); // aqui a variável nome é do tipo string